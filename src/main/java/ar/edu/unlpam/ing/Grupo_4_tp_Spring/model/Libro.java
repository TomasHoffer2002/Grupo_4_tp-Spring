package ar.edu.unlpam.ing.Grupo_4_tp_Spring.model;

public class Libro {

    private Long id;
    private String titulo;
    private String autor;
    private int anioPublicacion;

    // Constructor por defecto, necesario para frameworks como Spring.
    public Libro() {
    }

    // Constructor para inicializar un objeto Libro.
    public Libro(Long id, String titulo, String autor, int anioPublicacion) {
        this.id = id;
        this.titulo = titulo;
        this.autor = autor;
        this.anioPublicacion = anioPublicacion;
    }

    // Getters y Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public int getAnioPublicacion() {
        return anioPublicacion;
    }

    public void setAnioPublicacion(int anioPublicacion) {
        this.anioPublicacion = anioPublicacion;
    }
}